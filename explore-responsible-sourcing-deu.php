                                                        <p class="justify1">
                                                            Bio-Produkte unterliegen in Europa strengen Auflagen. Es dürfen beispielsweise keine
                                                            gentechnisch veränderten Organismen (GVO) und keine ionisierende Strahlung verwendet werden. GVO sind Organismen, bei denen das genetische Material in einer Weise verändert wurde, wie sie unter natürlichen Bedingungen durch Kreuzen oder natürliche Rekombination nicht vorkommt. Zudem ist ausschließlich ein begrenzter Einsatz von Kunstdünger, Herbiziden und Pestiziden erlaubt. Die Verwendung von Hormonen ist untersagt. Antibiotika dürfen nur eingeschränkt verwendet werden und auch nur dann, wenn es für die Gesundheit der Tiere notwendig ist. Diese Vorschriften dienen zum Schutz der Bodenfruchtbarkeit und der Gesundheit von Pflanzen und Tieren.
                                                        </p>
                                                        <p class="justify1">
                                                            Darum müssen die Betreibenden von Bio-Landwirtschaft ihre Betriebe anders führen, beispielsweise durch den Wechsel von angebauten Getreide-, Obst- und Gemüsesorten (eine andere Zucht pro Saison und Feld zur Erhaltung des Bodens). Es gilt den Anbau von stickstoffbindenden Pflanzen und anderen Güllepflanzen, die zur Wiederherstellung der Bodenfruchtbarkeit beitragen, sicher zu stellen und auf mineralischen Stickstoffdünger zu verzichten sowie die natürliche Schädlingsbekämpfung zu fördern. Dies ist wichtig für die resistenten Züchtungen und Techniken für eine natürliche Schädlingsbekämpfung, die Achtung der natürlichen Immunabwehr der Tiere und den Verzicht auf Massentierhaltung zur Erhaltung der Tiergesundheit.
